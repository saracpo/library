            if(tfGenre.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Niste uneli naziv žanra!", "Greška!!!", JOptionPane.ERROR_MESSAGE);
            } else {
                GenreDAO genreDao = GenreDAO.getInstance();
                genreDao.addGenre(new Genre(0, tfGenre.getText()));

                JOptionPane.showMessageDialog(this, "Uspešno unet žanr");
                MainForm.mainForm.changeContent(new AllGenresPanel());
            }
