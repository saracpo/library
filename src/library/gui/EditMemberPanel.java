    private void jbEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEditActionPerformed
        if(tfJMBG.getText().isEmpty() || tfAdress.getText().isEmpty() || tfName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nisu popunjena sva polja!", "Greška!!!", JOptionPane.ERROR_MESSAGE);
        } else if(tfJMBG.getText().length()!=13){
            JOptionPane.showMessageDialog(this, "Pogrešan JMBG!", "Greška!!!", JOptionPane.ERROR_MESSAGE);
        } else {
            try {                
                MemberDAO memberDao = MemberDAO.getInstance();
                m.name=tfName.getText();
                m.jmbg=tfJMBG.getText();
                m.adress=tfAdress.getText();
                memberDao.editMember(m);
                JOptionPane.showMessageDialog(this, "Član je uspešno izmenjen");
                MainForm.mainForm.changeContent(new AllMembersPanel());
            } catch (SQLException ex) {
                Logger.getLogger(EditMemberPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jbEditActionPerformed

    private void jbShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbShowActionPerformed
        try {
            MemberDAO memberDao = MemberDAO.getInstance();
            this.m = memberDao.getMember(Integer.parseInt(tfID.getText()));
            if(m==null) {
                JOptionPane.showMessageDialog(this, "Član ne postoji!", "Greška!!!", JOptionPane.ERROR_MESSAGE);
                tfName.setText("");
                tfJMBG.setText("");
                tfAdress.setText("");
            } else {
                tfName.setText(m.name);
                tfJMBG.setText(m.jmbg);
                tfAdress.setText(m.adress);
            }
        } catch (SQLException ex) {
            Logger.getLogger(EditMemberPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbShowActionPerformed


