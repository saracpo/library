    private void jbRentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbRentActionPerformed
        if(tfMember.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Niste uneli broj članske karte!", "Greška!!!", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                MemberDAO memberDao = MemberDAO.getInstance();
                Member m = memberDao.getMember(Integer.parseInt(tfMember.getText()));                
                m.rentBooks = memberDao.getRentBooksByMember(m.id);
                if(m.expiring.isBefore(LocalDate.now())) {
                    JOptionPane.showMessageDialog(this, "Ne može da se iznajmi! Istekla je članska karta", "Greška!!!", JOptionPane.ERROR_MESSAGE);
                } else if(m.expiring.isBefore(LocalDate.now().plusWeeks(2))) {
                        JOptionPane.showMessageDialog(this, "Ne može da se iznajmi! Mora da se produži članstvo", "Greška!!!", JOptionPane.ERROR_MESSAGE);                        
                } else if(m.rentBooks.size()>=3){
                    JOptionPane.showMessageDialog(this, "Ne može da se iznajmi! Prekoračen limit", "Greška!!!", JOptionPane.ERROR_MESSAGE);                    
                } else {
                    BookDAO bookDao = BookDAO.getInstance();
                    Book b = bookDao.getBook(id);
                    DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd.MM.yyyy.");                     
                    RentBookDAO rbDao = RentBookDAO.getInstance();
                    rbDao.rentBook(m, new RentBook(b, LocalDate.now(), LocalDate.now().plusWeeks(2),null,0));
                    JOptionPane.showMessageDialog(this, "Knjiga je uspešno iznajmljena");
                    MainForm.mainForm.changeContent(new AllBooksPanel());                    
                }
            } catch (SQLException ex) {
                Logger.getLogger(RentBookPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jbRentActionPerformed

    private void jbShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbShowActionPerformed
        try {
            MemberDAO memeberDao = MemberDAO.getInstance();
            Member m = memeberDao.getMember(Integer.parseInt(tfMember.getText()));
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd.MM.yyyy.");
            if(m == null) {
                    JOptionPane.showMessageDialog(this, "Ne postoji član!", "Greška!!!", JOptionPane.ERROR_MESSAGE);
            } else {
                jlName.setText(m.name);
                jlJMBG.setText(m.jmbg);
                jlStarted.setText(m.started.format(dtf));
                jlExpiring.setText(m.expiring.format(dtf));
            }
        } catch (SQLException ex) {
            Logger.getLogger(RentBookPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbShowActionPerformed


