    private void jbAddBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAddBookActionPerformed
        try {
            if(cbAuthors1.getSelectedIndex()==0 || cbGenres.getSelectedIndex()==0) {
                JOptionPane.showMessageDialog(this, "Pogrešno ste odabrali!", "Greška!!!", JOptionPane.ERROR_MESSAGE);
            } else if (tfTitle.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Niste uneli naslov knjige!", "Greška!!!", JOptionPane.ERROR_MESSAGE);
            } else {                
                    List<Author> authors = new ArrayList<>();
                    authors.add((Author)cbAuthors1.getSelectedItem());
                    if(cbAuthors2.isVisible()) {
                        authors.add((Author)cbAuthors2.getSelectedItem());
                    } 
                    if(cbAuthors3.isVisible()) {
                        authors.add((Author)cbAuthors3.getSelectedItem());
                    }
                    Book b = new Book(0,tfTitle.getText(), authors, ((Genre)cbGenres.getSelectedItem()), Integer.parseInt(jsStock.getValue().toString()));
                    BookDAO bookDao = BookDAO.getInstance();
                    bookDao.addBook(b);

                    for (Author author : authors) {
                        bookDao.addBooksAuthors(b.id, author.id);
                    }
                    JOptionPane.showMessageDialog(this, "Knjiga je uspešno uneta");
                    MainForm.mainForm.changeContent(new AllBooksPanel());
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(AddBookPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbAddBookActionPerformed

