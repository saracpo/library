    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(tfJMBG.getText().isEmpty() || tfAdress.getText().isEmpty() || tfName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nisu popunjena sva polja!", "Greška!!!", JOptionPane.ERROR_MESSAGE);
        } else if(tfJMBG.getText().length()!=13){
            JOptionPane.showMessageDialog(this, "Pogrešan JMBG!", "Greška!!!", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd.MM.yyyy.");
                MemberDAO memberDao = MemberDAO.getInstance();
                memberDao.addMember(new Member(0,tfName.getText(), tfJMBG.getText(), tfAdress.getText(),LocalDate.now(), LocalDate.now().plusYears(1), null));
                JOptionPane.showMessageDialog(this, "Član je uspešno unet");
                MainForm.mainForm.changeContent(new AllMembersPanel());
            } catch (SQLException ex) {
                Logger.getLogger(AddMemberPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed


