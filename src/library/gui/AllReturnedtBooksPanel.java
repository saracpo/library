    public AllReturnedtBooksPanel() {
        initComponents();
        SwingWorker sw = new SwingWorker() {
            @Override
            protected Object doInBackground() throws Exception {
                MemberDAO memberDao = MemberDAO.getInstance();
                List<Member> members = memberDao.getAllMembersReturnedBooks();
                return members;
            }

            @Override
            protected void done() {
                try {
                    List<Member> res = (List<Member>)get();
                    DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel();
                    DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd.MM.yyyy.");
                    for (Member m : res) {
                        dtm.addRow(new Object[]{
                            m.rentBooks.get(0).book.id, m.rentBooks.get(0).book.title, m.id, m.name, m.rentBooks.get(0).rentDate.format(dtf), m.rentBooks.get(0).returnDate.format(dtf), m.rentBooks.get(0).dateOfReturn.format(dtf), m.rentBooks.get(0).finePerBook
                        });
                    }
                } catch (InterruptedException | ExecutionException ex) {
                    Logger.getLogger(AllReturnedtBooksPanel.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            
        };
        sw.execute();
    }

